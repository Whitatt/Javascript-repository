function my_Function() {
    document.write(5 > 2 && 10 > 4);//code returns "true" because 5 is greater than 2 and 10 is greater than 4//
}

function my_Function1() {
    document.write(5 > 10 || 3 > 4);// false because 5 is not greater than 10 and 3 is not greater than 4//
}

//The && operator determines the logic between values or variables, such as verifying that ___ and ___ are true (both must be true//
 //to return “true.” If one or both is false, the code will return “false”)//